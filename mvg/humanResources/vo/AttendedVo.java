package humanResources.vo;

public class AttendedVo {
  private String attd_no;
  private String attd_emp;
  private String attd_date;
  private String attd_chk;
  private String attd_note;

  public AttendedVo() {
    super();
  }

  public AttendedVo(String attd_no, String attd_emp, String attd_date, String attd_chk, String attd_note) {
    super();
    this.attd_no = attd_no;
    this.attd_emp = attd_emp;
    this.attd_date = attd_date;
    this.attd_chk = attd_chk;
    this.attd_note = attd_note;
  }

  public String getAttd_no() {
    return attd_no;
  }

  public void setAttd_no(String attd_no) {
    this.attd_no = attd_no;
  }

  public String getAttd_emp() {
    return attd_emp;
  }

  public void setAttd_emp(String attd_emp) {
    this.attd_emp = attd_emp;
  }

  public String getAttd_date() {
    return attd_date;
  }

  public void setAttd_date(String attd_date) {
    this.attd_date = attd_date;
  }

  public String getAttd_chk() {
    return attd_chk;
  }

  public void setAttd_chk(String attd_chk) {
    this.attd_chk = attd_chk;
  }

  public String getAttd_note() {
    return attd_note;
  }

  public void setAttd_note(String attd_note) {
    this.attd_note = attd_note;
  }


}
