package humanResources.vo;

import javafx.scene.control.CheckBox;

public class RetiredEmployeeVo {
  private CheckBox remp_chk;
  private String remp_code;
  private String remp_emp;
  private String remp_name;
  private String remp_hp;
  private String remp_dept;
  private String remp_date;
  private String remp_note;

  public RetiredEmployeeVo() {
  }

  public RetiredEmployeeVo(CheckBox remp_chk, String remp_code, String remp_emp, String remp_name, String remp_hp, String remp_dept, String remp_date, String remp_note) {
    this.remp_chk = remp_chk;
    this.remp_code = remp_code;
    this.remp_emp = remp_emp;
    this.remp_name = remp_name;
    this.remp_hp = remp_hp;
    this.remp_dept = remp_dept;
    this.remp_date = remp_date;
    this.remp_note = remp_note;
  }

  public CheckBox getRemp_chk() {
    return remp_chk;
  }

  public void setRemp_chk(CheckBox remp_chk) {
    this.remp_chk = remp_chk;
  }

  public String getRemp_code() {
    return remp_code;
  }

  public void setRemp_code(String remp_code) {
    this.remp_code = remp_code;
  }

  public String getRemp_emp() {
    return remp_emp;
  }

  public void setRemp_emp(String remp_emp) {
    this.remp_emp = remp_emp;
  }

  public String getRemp_name() {
    return remp_name;
  }

  public void setRemp_name(String remp_name) {
    this.remp_name = remp_name;
  }

  public String getRemp_hp() {
    return remp_hp;
  }

  public void setRemp_hp(String remp_hp) {
    this.remp_hp = remp_hp;
  }

  public String getRemp_dept() {
    return remp_dept;
  }

  public void setRemp_dept(String remp_dept) {
    this.remp_dept = remp_dept;
  }

  public String getRemp_date() {
    return remp_date;
  }

  public void setRemp_date(String remp_date) {
    this.remp_date = remp_date;
  }

  public String getRemp_note() {
    return remp_note;
  }

  public void setRemp_note(String remp_note) {
    this.remp_note = remp_note;
  }
}