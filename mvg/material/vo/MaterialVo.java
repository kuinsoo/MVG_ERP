package material.vo;

public class MaterialVo {
  private String mat_code;
  private String mat_metc;
  private String mat_buyer;
  private String mat_dept;
  private String mat_date;
  private String mat_name;
  private int mat_qty;
  private int mat_price;
  private int mat_uprice;
  private String mat_stand;
  private String mat_note;

  public MaterialVo() {
  }

  public MaterialVo(String mat_code, String mat_metc, String mat_buyer, String mat_dept, String mat_date, String mat_name, int mat_qty, int mat_price, int mat_uprice, String mat_stand, String mat_note) {
    this.mat_code = mat_code;
    this.mat_metc = mat_metc;
    this.mat_buyer = mat_buyer;
    this.mat_dept = mat_dept;
    this.mat_date = mat_date;
    this.mat_name = mat_name;
    this.mat_qty = mat_qty;
    this.mat_price = mat_price;
    this.mat_uprice = mat_uprice;
    this.mat_stand = mat_stand;
    this.mat_note = mat_note;
  }

  public String getMat_code() {
    return mat_code;
  }

  public void setMat_code(String mat_code) {
    this.mat_code = mat_code;
  }

  public String getMat_metc() {
    return mat_metc;
  }

  public void setMat_metc(String mat_metc) {
    this.mat_metc = mat_metc;
  }

  public String getMat_buyer() {
    return mat_buyer;
  }

  public void setMat_buyer(String mat_buyer) {
    this.mat_buyer = mat_buyer;
  }

  public String getMat_dept() {
    return mat_dept;
  }

  public void setMat_dept(String mat_dept) {
    this.mat_dept = mat_dept;
  }

  public String getMat_date() {
    return mat_date;
  }

  public void setMat_date(String mat_date) {
    this.mat_date = mat_date;
  }

  public String getMat_name() {
    return mat_name;
  }

  public void setMat_name(String mat_name) {
    this.mat_name = mat_name;
  }

  public int getMat_qty() {
    return mat_qty;
  }

  public void setMat_qty(int mat_qty) {
    this.mat_qty = mat_qty;
  }

  public int getMat_price() {
    return mat_price;
  }

  public void setMat_price(int mat_price) {
    this.mat_price = mat_price;
  }

  public int getMat_uprice() {
    return mat_uprice;
  }

  public void setMat_uprice(int mat_uprice) {
    this.mat_uprice = mat_uprice;
  }

  public String getMat_stand() {
    return mat_stand;
  }

  public void setMat_stand(String mat_stand) {
    this.mat_stand = mat_stand;
  }

  public String getMat_note() {
    return mat_note;
  }

  public void setMat_note(String mat_note) {
    this.mat_note = mat_note;
  }
}