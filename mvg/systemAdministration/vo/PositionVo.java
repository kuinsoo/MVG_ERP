package systemAdministration.vo;

import javafx.scene.control.CheckBox;

/**
 * 직급VO
 * @author Sohyoung
 * @since 2018.09.17
 */
public class PositionVo {

	private CheckBox pos_chkbox;
	
	private String pos_code;
	private String pos_name;
	
	public PositionVo() {
		pos_chkbox = new CheckBox();
	}

	public PositionVo(String pos_code, String pos_name) {
		super();
		this.pos_code = pos_code;
		this.pos_name = pos_name;
	}

	public CheckBox getPos_chkbox() {
		return pos_chkbox;
	}
	
	public void setPpos_chkbox(CheckBox pos_chkbox) {
		this.pos_chkbox = pos_chkbox;
	}
	
	public String getPos_code() {
		return pos_code;
	}

	public void setPos_code(String pos_code) {
		this.pos_code = pos_code;
	}

	public String getPos_name() {
		return pos_name;
	}

	public void setPos_name(String pos_name) {
		this.pos_name = pos_name;
	}
}
